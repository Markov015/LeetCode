// 层次序遍历
class Solution {
    public Node connect(Node root) {
        levelOrder(root);
        return root;
    }

    private void levelOrder(Node root) {
        if (root == null) {
            return;
        }
        Queue<Node> queue = new LinkedList<>();
        queue.add(root);
        while (!queue.isEmpty()) {
            int size = queue.size();
            for (int i = 0; i < size; i++) {
                Node curr = queue.remove();
                curr.next = i < size - 1 ? queue.element() : null;
                if (curr.left != null) {
                    queue.add(curr.left);
                }
                if (curr.right != null) {
                    queue.add(curr.right);
                }
            }
        }
    }
}

